#!/usr/bin/env bash
# Запуск симуляции нагрузки на CPU и RAM
set -euo pipefail

# Защита от повторного запуска без очистки
if pgrep -f 'stress-ng --cpu 2 --vm 1 --vm-bytes 500M' >/dev/null 2>&1; then
  echo "Симуляция нагрузки уже запущена! Сбросьте окружение перед запуском: ./cleanup.sh"
  exit 0
fi

echo "Запускаем скрытую нагрузку на CPU и RAM..."
# Нагружаем 2 ядра на 100% и забиваем 500МБ памяти, прячем процесс в фоне на 1 час
stress-ng --cpu 2 --vm 1 --vm-bytes 500M -t 3600 &>/dev/null &

echo "Нагрузка успешно запущена в фоне. Ваша задача — найти и остановить процесс!"
